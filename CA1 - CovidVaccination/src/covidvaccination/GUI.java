    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
    // Adding patients's names, age and if condition is has any medical condition or not (true or false)
        
        // This if statement was made to inform user that is necessary to fill all information for registration
        if(nameTxtField.getText().isEmpty() || ageTxtField.getText().isEmpty() || (radioYes.isSelected()==false && radioNo.isSelected()==false)){   
            JOptionPane.showMessageDialog(this, "You must fill all information");
        } else {
            Patient pt = new Patient();

            // Taking name from Text Field and assigning to variables
            name = nameTxtField.getText();
            ageINT = new Integer (ageTxtField.getText());
            age = ageINT;

            if(radioYes.isSelected()){
                condition = true;
            }else{
                condition = false;
            }
            
            pt.setName(name);
            pt.setAge(age);
            pt.setCondition(condition);

            // Adding to queue
            all.enqueue(pt);

            // Display item added
            textArea.setText("");
            textArea.append("Patient " + pt.getName() + " was added successfully");


            // Add to priority queue withouth index yet
            myPQ.enqueue(pt);


            // Clear all input areas
            nameTxtField.setText("");
            ageTxtField.setText("");
            radioYes.setSelected(false);
            radioNo.setSelected(false);
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void nextGroupBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextGroupBtnActionPerformed
    //  It will show next group to be vaccinated according to their priority
    
        if(!myPQ.isEmpty()){
            
            // This should get the first element key on the priority queue
            PQElement headElement;
            headElement = myPQ.getElement(0);
            int firstKey = headElement.getKey();

            do{
                Patient temp = (Patient)headElement.getElement();
                name = temp.getName();
                age = temp.getAge();
                condition = temp.condition;

                myPQ.dequeue();


                if(myPQ.isEmpty() == false){
                    PQElement nextElement = myPQ.getElement(0);
                    newKey = nextElement.getKey();
                }

            }while(newKey == firstKey && myPQ.isEmpty() == false);


            // This part check which group is the next one to be called to be vaccinated

            // String to hold output message
            String nextGroup = "";

            if(firstKey == 10){
                nextGroup = "Next priority group is 10 - People aged 90 and older";
            }else if(firstKey == 9) {
                nextGroup = "Next priority group is 9 - People aged 80 and older";
            }else if(firstKey == 8){
                nextGroup = "Next priority group is 8 - People aged 70 and older";
            }else if(firstKey == 7){
                nextGroup = "Next priority group is 7 - People aged 65 and older";
            }else if(firstKey == 6){
                nextGroup = "Next priority group is 6 - People aged 18-64 with medical condition(s)";
            }else if(firstKey == 5){
                nextGroup = "Next priority group is 5 - People aged 55-64";
            }else if(firstKey == 4){
                nextGroup = "Next priority group is 4 - People aged 45-54";
            }else if(firstKey == 3){
                nextGroup = "Next priority group is 3 - People aged 30-44";
            }else if(firstKey == 2){
                nextGroup = "Next priority group is 2 - People aged 18-29";
            }else if(firstKey == 1){
                nextGroup = "Next priority group is 1 - People aged under 18";
            }else{
                nextGroup = "No more patients to be vaccinated";
            }

            textArea.setText(""); // To clear text area before new output
            textArea.append(nextGroup); // Displays next group to be called
        
        } else{
            textArea.setText(""); // To clear text area before new output
            textArea.append("No one to be vaccinated");
        }    
            
        
    }//GEN-LAST:event_nextGroupBtnActionPerformed

